/*
 * 0 terminate string stream, reset, and return value
 */

char*
costash(Sfio_t* sp)
{
	char*			s;

	if (!(s = sfstruse(sp)))
		errormsg(state.lib, ERROR_LIBRARY|2, "out of space");
	return s;
}
